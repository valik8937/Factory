    public static void DrawString(SpriteBatch sb, Texture2D pixel, string text, Vector2 position, Color color, int scale = 2)
    {
        Vector2 cursor = position;
        text = text.ToUpper();

        foreach (char c in text)
        {
            char lookupChar = c;
            if (!Glyphs.ContainsKey(lookupChar))
            {
                lookupChar = ' ';
            }

            if (Glyphs.TryGetValue(lookupChar, out byte[] rows))
            {
                for (int y = 0; y < 7; y++)
                {
                    byte row = rows[y];
                    for (int x = 0; x < 5; x++)
                    {
                        if (((row >> (4 - x)) & 1) == 1)
                        {
                            sb.Draw(pixel, new Rectangle(
                                (int)(cursor.X + x * scale),
                                (int)(cursor.Y + y * scale),
                                scale, scale), color);
                        }
                    }
                }
            }
            cursor.X += 6 * scale;
        }
    }
